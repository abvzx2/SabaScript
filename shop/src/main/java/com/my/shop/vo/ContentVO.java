package com.my.shop.vo;

public class ContentVO {
	private int contentNum;
	private String contentName, contentThumbnail, contentUrl, contentDes;
	public int getContentNum() {
		return contentNum;
	}
	public void setContentNum(int contentNum) {
		this.contentNum = contentNum;
	}
	public String getContentName() {
		return contentName;
	}
	public void setContentName(String contentName) {
		this.contentName = contentName;
	}
	public String getContentThumbnail() {
		return contentThumbnail;
	}
	public void setContentThumbnail(String contentThumbnail) {
		this.contentThumbnail = contentThumbnail;
	}
	public String getContentUrl() {
		return contentUrl;
	}
	public void setContentUrl(String contentUrl) {
		this.contentUrl = contentUrl;
	}
	public String getContentDes() {
		return contentDes;
	}
	public void setContentDes(String contentDes) {
		this.contentDes = contentDes;
	}
	

}
